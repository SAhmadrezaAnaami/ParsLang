def Persian2English(current_char):
    if current_char == '۰':
        return "0"
    elif current_char == '۱':
        return "1"
    elif current_char == '۲':
        return "2"
    elif current_char == '۳':
        return "3"
    elif current_char == '۴':
        return "4"
    elif current_char == '۵':
        return "5"
    elif current_char == '۶':
        return "6"
    elif current_char == '۷':
        return "7"
    elif current_char == '۸':
        return "8"
    elif current_char == '۹':
        return "9"
    else:
        return current_char